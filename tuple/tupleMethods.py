# tuple has only two methods 
#1. count() - which counts a number or an element which appears how many times in tuple
#2. index() - which  returns a index value of according to the existing element in tuple
tuple=(10,20,30,40,50,60,70,80,90,10,30)
print(tuple.count(10)) #2 refers to 10 appears two times in tuple
print(tuple.index(30)) #2 refers to 30 value is in index number 
#--> Adding an element to set
set={10,20,30,40,50}
set.add(60)
print(set)

#-->removing an element from set
sets={60,50,40,30,20,10}
sets.remove(60)
print(sets)

#also we use discard it uses better whenever you want to remove element id not there it doesnt occurs any other
sets.discard(10)

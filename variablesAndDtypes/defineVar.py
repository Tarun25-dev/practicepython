#1. Variable name must start with a-z (or) A-Z (or) underscore(_)
#2. After first charecter you can use  Numbers
#3. No spaces allowed
#4. Variable names are case sensitive(differ upper and lowercase letter)
#5. Cannot use python keywords 
#6. No special charecters except underscore(_)
#7. No need to declare datatype to a variable

